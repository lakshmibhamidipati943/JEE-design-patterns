package mvc;
//Model class

public class Student {

	private String name;
	private String rollNo;
	public String getName() {
		return name;
	}
	public String getRollNo() {
		return rollNo;
	}
	public void setName(String name) {
		this.name=name;
	}
	public void setRollNo(String rollNo) {
		this.rollNo=rollNo;
	}
}
